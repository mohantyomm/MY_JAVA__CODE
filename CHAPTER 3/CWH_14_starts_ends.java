public class CWH_14_starts_ends {
    public static void main(String[] args) {
        String name = "chiku";
        System.out.println(name.startsWith("ch"));
        System.out.println(name.startsWith("ku"));
        System.out.println(name.endsWith("iku"));
        System.out.println(name.endsWith("dku"));
    }
}
