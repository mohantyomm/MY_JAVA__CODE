public class CWH_02_variables {
    public static void main(String[] args) {
        int a = 656;
        short g = 6;
        byte b = 122;// limit between -127 to 128.
        double d = 26.23;
        long e = 53126482487l;
        float f = 6.227462f;
        char c = 'A';
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        // Thereis no such thing to print new line directly
        // so we have to use println to create a new line..
        // but we can use \n iff we use printf or format in the place of println
    }
}
