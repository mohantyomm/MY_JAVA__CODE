public class add_sub_mul_div{
    public static void main(String[] args) {
        int a =3;
        int b =6;
        int add = a+b;
        int sub = b-a;
        int mul = a*b;
        int div = b/a;
        System.out.println("The addition of two element is = "+add);
        System.out.println("The substraction of two element is = "+sub);
        System.out.println("The multiplication of two element is = "+mul);
        System.out.println("The division of two element is = "+div);
    }
}